    public PnlFisRestriccion6DOF(QRestriccionGenerica6DOF componente) {
        initComponents();
        this.componente = componente;

        DefaultComboBoxModel modelo = new DefaultComboBoxModel<>();
        for (QEntidad entidad : Principal.instancia.getEscena().getListaEntidades()) {
            for (QComponente comp : entidad.getComponentes()) {
                if (comp instanceof QObjetoRigido) {
                    modelo.addElement(entidad.getNombre());
                    mapa.put(entidad.getNombre(), (QObjetoRigido) comp);
                }
            }
        }
        cboFisico.setModel(modelo);
        if (componente.getB() != null) {
            cboFisico.setSelectedItem(componente.getB().entidad.getNombre());
        }
        chkUsarReferenciaLinear.setSelected(componente.isUsarReferenciaLinearA());
        aplicarCambios();
//        cboFisico.setSelectedItem(forma.getMalla().nombre);
//        txtRadio.setText(String.valueOf(esfera.getRadio()));
    }

    private void aplicarCambios() {
        componente.setB(mapa.get(cboFisico.getSelectedItem().toString()));
        componente.setUsarReferenciaLinearA(chkUsarReferenciaLinear.isSelected());
//        componente.setRadio(Float.parseFloat(txtRadio.getText()));
    }

