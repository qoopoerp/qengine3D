    public PnlColisionMallaConvexa(QColisionMallaConvexa forma) {
        initComponents();
        this.forma = forma;
        DefaultComboBoxModel modelo = new DefaultComboBoxModel<>();
        for (QComponente comp : forma.entidad.getComponentes()) {
            if (comp instanceof QGeometria) {
                modelo.addElement(((QGeometria) comp).nombre);
            }
        }
        cboMalla.setModel(modelo);
        cboMalla.setSelectedItem(forma.getMalla().nombre);
//        txtRadio.setText(String.valueOf(forma.getRadio()));
    }

    private void aplicarCambios() {
//        forma.setRadio(Float.parseFloat(txtRadio.getText()));
        for (QComponente comp : forma.entidad.getComponentes()) {
            if (comp instanceof QGeometria) {
                if (((QGeometria) comp).nombre.equals(cboMalla.getSelectedItem().toString())) {
                    forma.setMalla((QGeometria) comp);
                }
            }
        }
    }

