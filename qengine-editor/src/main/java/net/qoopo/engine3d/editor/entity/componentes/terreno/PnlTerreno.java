        private void aplicarCambios() {
                // elimino los componentes de geometria que se haya generado anteriormente
                ComponentUtil.removeComponents(terreno.getEntity(), Mesh.class);
                if (textura != null) {
                        textura.setMuestrasU(Float.valueOf(txtMU_Difusa.getText()));
                        textura.setMuestrasV(Float.valueOf(txtMV_Difusa.getText()));
                }
                if (imagen != null) {
                        terreno.setImagen(imagen);
                        terreno.setTileSize(Float.valueOf(txtTamCelda.getText()));
                        terreno.setMinY(Float.valueOf(txtMinY.getText()));
                        terreno.setMaxY(Float.valueOf(txtMaxY.getText()));
                        terreno.getMaterial().setMapaColor(textura);
                        terreno.setOffset(Integer.valueOf(txtOffset.getText()));
                        terreno.build();
                }
        }

